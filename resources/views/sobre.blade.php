    <h1>Regras para utilização do serviço</h1>
<p>- O horário de funcionamento das estações é das 05h às 23h todos os dias da semana.</p>
<p>- Taxas de não respeito aos prazos: Após os 60 primeiros minutos, será cobrado o valor de R$ 5 adicional a cada período
