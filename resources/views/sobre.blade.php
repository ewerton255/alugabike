    a 15 minutos.
</p>
<p>- Taxa de danos à bicicleta: Será cobrada uma taxa de R$ 200 caso a bicicleta seja devolvida com danos estruturais comprovadamente
    causados pelo usuário.
</p>
<p>- Taxa de não devolução: Se constatado a não devolução da bicicleta até 72 horas depois de finalizado o empréstimo, será
    cobrado uma taxa de R$ 2 mil no cartão do usuário ou via boleto de cobrança.
</p>
<p>- Limite do cartão: Ao contratar um plano diário ou de três dias será feita uma pré-autorização no cartão do usurário
    no valor de R$ 300. O valor que não é debitado, mas impacta no limite do cartão, é liberado em até quatro dias depois
    da devolução da bicicleta.
</p>
<p>
    - Tarifa de emissão de cartão: Será cobrada uma tarifa de R$ 5 para a emissão de cartão quando o usuário optar por receber
    u cartão magnético do Sistema Bike Itaú na compra de um plano mensal, através do site.
</p>
<p>
    - A contratação presencial em um dos pontos de atendimento físico só pode ser efetuada em dinheiro. E o usuário deverá oferecer
    documento original com foto e comprovante de residência (conta de consumo).
</p>
<p>
    - Uso do cartão do transporte público: Após adquirir o Plano desejado, o usuário poderá cadastrar seu cartão de transporte
    “Bilhete Único” pelo site ou pela central de atendimento, para retirar as bikes nas estações.
